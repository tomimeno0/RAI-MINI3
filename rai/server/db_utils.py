DEFAULT_SERVER_DB_PATH = Path(cli_init_db.DEFAULT_DB_PATH)
ENV_DB_PATH = os.environ.get("RAI_SERVER_DB_PATH")
DB_PATH = Path(ENV_DB_PATH) if ENV_DB_PATH else DEFAULT_SERVER_DB_PATH

_LOGGER = get_logger(__name__)


def ensure_schema(path: Path | None = None) -> None:
    """Ensure the database schema exists by applying pending migrations."""

    target = Path(path) if path else DB_PATH
    cli_init_db.apply_migrations(db_path=target, logger=_LOGGER)


def load_apps(path: Path | None = None) -> List[Dict[str, object]]:
    """Return the active installs used by the interpreter catalogue."""

    target = Path(path) if path else DB_PATH
    ensure_schema(target)
    with sqlite3.connect(target) as conn:
        conn.row_factory = sqlite3.Row
        cursor = conn.execute(
            """
            SELECT
                lower(ac.display_name) AS normalized_name,
                ac.display_name,
                CASE
                    WHEN inst.package_id IS NOT NULL THEN 'uwp'
                    ELSE inst.source
                END AS app_type,
                bin.exe_path,
                inst.source,
                pkg.package_fullname AS app_id,
                inst.last_seen_at,
                host.hostname
            FROM installs AS inst
            JOIN hosts AS host ON host.id = inst.host_id
            JOIN apps_catalog AS ac ON ac.id = inst.app_catalog_id
            LEFT JOIN packages AS pkg ON pkg.id = inst.package_id
            LEFT JOIN binaries AS bin ON bin.id = inst.binary_id
            WHERE inst.is_active = 1 AND inst.removed_at IS NULL
            ORDER BY inst.last_seen_at DESC
            """
        )
        apps: List[Dict[str, object]] = []
        for row in cursor.fetchall():
            apps.append(
                {
                    "name": row["normalized_name"],
                    "display_name": row["display_name"],
                    "type": row["app_type"],
                    "exe_path": row["exe_path"],
                    "app_id": row["app_id"],
                    "source": row["source"],
                    "last_seen": row["last_seen_at"],
                    "hostname": row["hostname"],
                }
            )
        return apps


def ingest_scan(
    hostname: str,
    apps: Sequence[Mapping[str, object]],
    *,
    db_path: Path | None = None,
    trace_id: str | None = None,
    logger: logging.Logger | None = None,
) -> int:
    """Persist a scan payload generated by the client scanner."""

    if not isinstance(hostname, str) or not hostname.strip():
        raise ValueError("hostname must be a non-empty string")

    target = Path(db_path) if db_path else DB_PATH
    ensure_schema(target)

    log = logger or _LOGGER
    normalised_host = hostname.strip()

    processed = 0
    with _connect(target) as conn:
        host_id = _upsert_host(conn, normalised_host)
        scan_id = _start_scan(conn, host_id)
        dedupe: set[str] = set()

        for item in apps:
            if not isinstance(item, Mapping):
                continue
            record = _prepare_record(item)
            if record is None:
                continue

            identity = record.pop("identity", None)
            if identity:
                if identity in dedupe:
                    continue
                dedupe.add(identity)

            if _insert_scan_item(conn, host_id, scan_id, record):
                processed += 1

        _finalise_scan(conn, scan_id, processed)

    log.info(
        "ingest_scan completed",
        extra={"host": normalised_host, "items": processed, "trace_id": trace_id},
    )
    return processed


def scan_and_update_db(path: Path | None = None) -> int:
    """Execute a local scan using the client module and persist the results."""

    target = Path(path) if path else DB_PATH
    ensure_schema(target)

    from ..client import scanner  # Lazy import avoids Windows-only deps at import time

    try:
        apps = scanner.scan_apps(full=True)
    except Exception as exc:  # pragma: no cover - scanner is Windows-specific
        _LOGGER.warning("scan_apps failed", extra={"error": str(exc)})
        raise

    hostname = socket.gethostname()
    return ingest_scan(hostname, apps, db_path=target, logger=_LOGGER)


